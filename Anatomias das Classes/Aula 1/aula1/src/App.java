public class App {
    /*se a classe é executavel ela precisa ter um metodo principal
    sendo ele o main */
    public static void main(String [] args){
        /* void quer dizer que o metodo não retorna nada, apenas executa
        dentro dos paramentros -o parenteses- ele espera um parametro especial,
         nesse caso String
        args = argumentos
        */
        System.out.println("Hello world");
        /* todo método precisa de um parâmetro */
    }

}
